

# Functions in Python
# There's two type of Functions in Python - 
# 1. Inbuilt Functions
# 2. User-Defined Functions

# 1. Simple Function

def helloWorld():
    print('Hello,World!')


helloWorld()

# 2. Function with Arguments

def helloWorld(name):
    print('Greetings',name)

helloWorld('Bishnudev')

# 3. Return-Type Function

def helloWorld():
    return 'Hello Everyone'

print(helloWorld())